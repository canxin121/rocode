//! Recovery protocol — formal types, prompt composition, and protocol assembly.
//!
//! This module is the single authority for recovery semantics (Constitution §5).
//! Route handlers import from here; they never define recovery types inline.

use serde::{Deserialize, Serialize};

use crate::runtime_control::{
    ExecutionKind, ExecutionStatus, SessionExecutionNode, SessionExecutionTopology,
};

// ---------------------------------------------------------------------------
// Public protocol types
// ---------------------------------------------------------------------------

/// Overall status of a session's recovery protocol.
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq)]
#[serde(rename_all = "snake_case")]
pub enum RecoveryProtocolStatus {
    /// Active executions are still running.
    Running,
    /// Pending questions must be answered before recovery can proceed.
    AwaitingUser,
    /// Recovery actions are available for selection.
    Recoverable,
    /// No recovery target was inferred.
    Idle,
}

/// Kind of recovery action the user can invoke.
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq)]
#[serde(rename_all = "snake_case")]
pub enum RecoveryActionKind {
    AbortRun,
    AbortStage,
    Retry,
    Resume,
    PartialReplay,
    RestartStage,
    RestartSubtask,
}

/// Checkpoint information attached to a recoverable boundary.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct RecoveryCheckpointInfo {
    pub id: String,
    pub kind: String,
    pub label: String,
    pub status: String,
    #[serde(skip_serializing_if = "Option::is_none")]
    pub summary: Option<String>,
    #[serde(skip_serializing_if = "Option::is_none")]
    pub scheduler_profile: Option<String>,
    #[serde(skip_serializing_if = "Option::is_none")]
    pub stage: Option<String>,
    #[serde(skip_serializing_if = "Option::is_none")]
    pub stage_index: Option<u32>,
    #[serde(skip_serializing_if = "Option::is_none")]
    pub stage_total: Option<u32>,
}

/// A single recovery action that the protocol currently permits.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct RecoveryActionInfo {
    pub kind: RecoveryActionKind,
    pub label: String,
    pub description: String,
    #[serde(skip_serializing_if = "Option::is_none")]
    pub target_id: Option<String>,
    #[serde(skip_serializing_if = "Option::is_none")]
    pub target_kind: Option<String>,
}

/// Full recovery protocol snapshot for a session.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct SessionRecoveryProtocol {
    pub session_id: String,
    pub status: RecoveryProtocolStatus,
    pub active_execution_count: usize,
    pub pending_question_count: usize,
    #[serde(skip_serializing_if = "Option::is_none")]
    pub summary: Option<String>,
    #[serde(skip_serializing_if = "Option::is_none")]
    pub last_user_prompt: Option<String>,
    #[serde(default)]
    pub actions: Vec<RecoveryActionInfo>,
    #[serde(default)]
    pub checkpoints: Vec<RecoveryCheckpointInfo>,
}

/// Incoming request to execute a recovery action.
#[derive(Debug, Deserialize)]
pub struct ExecuteRecoveryRequest {
    pub action: RecoveryActionKind,
    #[serde(default)]
    pub target_id: Option<String>,
}

/// Internal context attached to a prompt that was generated by a recovery action.
#[derive(Debug, Clone, Default, Serialize, Deserialize)]
pub(crate) struct RecoveryExecutionContext {
    pub action: Option<RecoveryActionKind>,
    pub target_id: Option<String>,
    pub target_kind: Option<String>,
    pub target_label: Option<String>,
}

