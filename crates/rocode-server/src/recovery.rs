pub(crate) fn collect_stage_recovery_targets(
    session: &rocode_session::Session,
) -> Vec<StageRecoveryTarget> {
    session
        .messages
        .iter()
        .rev()
        .filter_map(|message| {
            #[derive(Debug, Default, serde::Deserialize)]
            struct StageRecoveryMetadataWire {
                #[serde(
                    default,
                    deserialize_with = "rocode_types::deserialize_opt_string_lossy"
                )]
                scheduler_stage: Option<String>,
                #[serde(
                    default,
                    deserialize_with = "rocode_types::deserialize_opt_string_lossy"
                )]
                resolved_scheduler_profile: Option<String>,
                #[serde(
                    default,
                    deserialize_with = "rocode_types::deserialize_opt_string_lossy"
                )]
                scheduler_profile: Option<String>,
                #[serde(
                    default,
                    deserialize_with = "rocode_types::deserialize_opt_string_lossy"
                )]
                scheduler_stage_status: Option<String>,
                #[serde(
                    default,
                    deserialize_with = "rocode_types::deserialize_opt_string_lossy"
                )]
                scheduler_stage_title: Option<String>,
                #[serde(
                    default,
                    deserialize_with = "rocode_types::deserialize_opt_string_lossy"
                )]
                scheduler_stage_focus: Option<String>,
                #[serde(
                    default,
                    deserialize_with = "rocode_types::deserialize_opt_u64_lossy"
                )]
                scheduler_stage_index: Option<u64>,
                #[serde(
                    default,
                    deserialize_with = "rocode_types::deserialize_opt_u64_lossy"
                )]
                scheduler_stage_total: Option<u64>,
            }

            let meta: StageRecoveryMetadataWire = rocode_types::parse_map_lossy(&message.metadata);
            let stage = meta.scheduler_stage?;
            let profile = meta.resolved_scheduler_profile.or(meta.scheduler_profile);
            let status = meta
                .scheduler_stage_status
                .unwrap_or_else(|| "done".to_string());
            let label = meta
                .scheduler_stage_title
                .or_else(|| meta.scheduler_stage_focus.map(|focus| format!("{stage} · {focus}")))
                .unwrap_or_else(|| stage.clone());
            let body = assistant_visible_text_from_message(message);
            Some(StageRecoveryTarget {
                checkpoint: RecoveryCheckpointInfo {
                    id: message.id.clone(),
                    kind: "stage".to_string(),
                    label,
                    status,
                    summary: short_recovery_text(&body, 240),
                    scheduler_profile: profile,
                    stage: Some(stage),
                    stage_index: meta.scheduler_stage_index.map(|value| value as u32),
                    stage_total: meta.scheduler_stage_total.map(|value| value as u32),
                },
                body,
            })
        })
        .collect()
}

pub(crate) fn collect_subtask_recovery_targets(
    session: &rocode_session::Session,
) -> Vec<SubtaskRecoveryTarget> {
    let mut targets = Vec::new();
    for message in session.messages.iter().rev() {
        if !matches!(message.role, rocode_session::MessageRole::User) {
            continue;
        }
        for part in message.parts.iter().rev() {
            if let rocode_session::PartType::Subtask {
                id,
                description,
                status,
            } = &part.part_type
            {
                targets.push(SubtaskRecoveryTarget {
                    checkpoint: RecoveryCheckpointInfo {
                        id: id.clone(),
                        kind: "subtask".to_string(),
                        label: if description.trim().is_empty() {
                            format!("Subtask {id}")
                        } else {
                            description.clone()
                        },
                        status: status.clone(),
                        summary: Some(format!("Subtask is currently `{status}`.")),
                        scheduler_profile: None,
                        stage: None,
                        stage_index: None,
                        stage_total: None,
                    },
                    description: description.clone(),
                });
            }
        }
        if !targets.is_empty() {
            break;
        }
    }
    targets
}

// ---------------------------------------------------------------------------
// Active stage checkpoint from topology
// ---------------------------------------------------------------------------

pub(crate) fn find_active_scheduler_stage_checkpoint(
    topology: &SessionExecutionTopology,
) -> Option<RecoveryCheckpointInfo> {
    let mut best: Option<&SessionExecutionNode> = None;
    fn visit<'a>(node: &'a SessionExecutionNode, best: &mut Option<&'a SessionExecutionNode>) {
        if matches!(node.kind, ExecutionKind::SchedulerStage)
            && matches!(
                node.status,
                ExecutionStatus::Running
                    | ExecutionStatus::Waiting
                    | ExecutionStatus::Cancelling
                    | ExecutionStatus::Retry
            )
        {
            let replace = match best {
                Some(current) => node.updated_at > current.updated_at,
                None => true,
            };
            if replace {
                *best = Some(node);
            }
        }
        for child in &node.children {
            visit(child, best);
        }
    }
    for root in &topology.roots {
        visit(root, &mut best);
    }
    best.map(|node| {
        #[derive(Debug, Default, serde::Deserialize)]
        struct StageNodeMetadataWire {
            #[serde(
                default,
                deserialize_with = "rocode_types::deserialize_opt_string_lossy"
            )]
            scheduler_profile: Option<String>,
            #[serde(
                default,
                deserialize_with = "rocode_types::deserialize_opt_string_lossy"
            )]
            scheduler_stage: Option<String>,
            #[serde(
                default,
                deserialize_with = "rocode_types::deserialize_opt_u64_lossy"
            )]
            scheduler_stage_index: Option<u64>,
            #[serde(
                default,
                deserialize_with = "rocode_types::deserialize_opt_u64_lossy"
            )]
            scheduler_stage_total: Option<u64>,
        }

        let meta: StageNodeMetadataWire = node
            .metadata
            .as_ref()
            .map(rocode_types::parse_value_lossy)
            .unwrap_or_default();

        RecoveryCheckpointInfo {
        id: node.id.clone(),
        kind: "stage".to_string(),
        label: node.label.clone().unwrap_or_else(|| node.id.clone()),
        status: match node.status {
            ExecutionStatus::Running => "running",
            ExecutionStatus::Waiting => "waiting",
            ExecutionStatus::Cancelling => "cancelling",
            ExecutionStatus::Retry => "retry",
            ExecutionStatus::Done => "done",
        }
        .to_string(),
        summary: node.recent_event.clone(),
        scheduler_profile: meta.scheduler_profile,
        stage: meta.scheduler_stage,
        stage_index: meta.scheduler_stage_index.map(|value| value as u32),
        stage_total: meta.scheduler_stage_total.map(|value| value as u32),
        }
    })
}

