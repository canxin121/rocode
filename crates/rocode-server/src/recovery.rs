/// Check whether `protocol` currently advertises the given action + target.
pub(crate) fn protocol_allows_recovery_action(
    protocol: &SessionRecoveryProtocol,
    action: &RecoveryActionKind,
    target_id: Option<&str>,
) -> bool {
    protocol.actions.iter().any(|candidate| {
        if &candidate.kind != action {
            return false;
        }
        match (&candidate.target_id, target_id) {
            (Some(expected), Some(actual)) => expected == actual,
            (Some(_), None) => false,
            (None, Some(_)) => false,
            (None, None) => true,
        }
    })
}

/// Build the full recovery protocol snapshot for a session.
pub(crate) fn build_session_recovery_protocol(
    session_id: &str,
    session: &rocode_session::Session,
    topology: &SessionExecutionTopology,
    pending_question_count: usize,
) -> SessionRecoveryProtocol {
    let last_user_prompt = latest_user_prompt(session);
    let stage_targets = collect_stage_recovery_targets(session);
    let subtask_targets = collect_subtask_recovery_targets(session);
    let finish = latest_assistant_finish(session);
    let mut actions = Vec::new();
    let mut checkpoints = Vec::new();

    checkpoints.extend(stage_targets.iter().map(|target| target.checkpoint.clone()));
    checkpoints.extend(
        subtask_targets
            .iter()
            .map(|target| target.checkpoint.clone()),
    );

    let busy = topology.active_count > 0;
    if busy || pending_question_count > 0 {
        actions.push(RecoveryActionInfo {
            kind: RecoveryActionKind::AbortRun,
            label: "Abort run".to_string(),
            description: "Stop the current session run and return control to recovery mode."
                .to_string(),
            target_id: None,
            target_kind: None,
        });
        if let Some(stage) = find_active_scheduler_stage_checkpoint(topology) {
            actions.push(RecoveryActionInfo {
                kind: RecoveryActionKind::AbortStage,
                label: format!("Abort stage · {}", stage.label),
                description:
                    "Cancel only the current scheduler stage and keep the wider session available for recovery."
                        .to_string(),
                target_id: Some(stage.id),
                target_kind: Some("stage".to_string()),
            });
        }
    }

    if !busy && pending_question_count == 0 {
        if last_user_prompt.is_some() && matches!(finish.as_deref(), Some("error" | "cancelled")) {
            actions.push(RecoveryActionInfo {
                kind: RecoveryActionKind::Retry,
                label: "Retry last run".to_string(),
                description: "Re-run the last request with the same agent or preset configuration."
                    .to_string(),
                target_id: None,
                target_kind: None,
            });
        }
        if last_user_prompt.is_some() {
            actions.push(RecoveryActionInfo {
                kind: RecoveryActionKind::Resume,
                label: "Resume from latest boundary".to_string(),
                description:
                    "Continue from the most recent incomplete boundary without re-discovering the whole task."
                        .to_string(),
                target_id: None,
                target_kind: None,
            });
        }
        if let Some(stage) = stage_targets
            .iter()
            .find(|target| target.checkpoint.status != "done")
            .or_else(|| stage_targets.first())
        {
            actions.push(RecoveryActionInfo {
                kind: RecoveryActionKind::RestartStage,
                label: format!("Restart stage · {}", stage.checkpoint.label),
                description:
                    "Restart from this scheduler stage and replay only the necessary downstream work."
                        .to_string(),
                target_id: Some(stage.checkpoint.id.clone()),
                target_kind: Some("stage".to_string()),
            });
            actions.push(RecoveryActionInfo {
                kind: RecoveryActionKind::PartialReplay,
                label: format!("Partial replay · {}", stage.checkpoint.label),
                description:
                    "Replay from this stage boundary while preserving already valid prior work."
                        .to_string(),
                target_id: Some(stage.checkpoint.id.clone()),
                target_kind: Some("stage".to_string()),
            });
        }
        if let Some(subtask) = subtask_targets
            .iter()
            .find(|target| target.checkpoint.status != "completed")
            .or_else(|| subtask_targets.first())
        {
            actions.push(RecoveryActionInfo {
                kind: RecoveryActionKind::RestartSubtask,
                label: format!("Restart subtask · {}", subtask.checkpoint.label),
                description:
                    "Restart just this subtask boundary and keep other completed work intact."
                        .to_string(),
                target_id: Some(subtask.checkpoint.id.clone()),
                target_kind: Some("subtask".to_string()),
            });
        }
    }

    let (status, summary) = if busy {
        (
            RecoveryProtocolStatus::Running,
            Some(format!(
                "{} active execution(s) are still running; recovery actions unlock after the current run stops.",
                topology.active_count
            )),
        )
    } else if pending_question_count > 0 {
        (
            RecoveryProtocolStatus::AwaitingUser,
            Some(format!(
                "{} pending question(s) need answers before recovery can continue.",
                pending_question_count
            )),
        )
    } else if !actions.is_empty() {
        (
            RecoveryProtocolStatus::Recoverable,
            Some(
                "Recovery actions are available at run, stage, and subtask granularity."
                    .to_string(),
            ),
        )
    } else {
        (
            RecoveryProtocolStatus::Idle,
            Some("No active recovery target was inferred for this session.".to_string()),
        )
    };

    SessionRecoveryProtocol {
        session_id: session_id.to_string(),
        status,
        active_execution_count: topology.active_count,
        pending_question_count,
        summary,
        last_user_prompt,
        actions,
        checkpoints,
    }
}

