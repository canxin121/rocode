pub(crate) fn compose_retry_prompt(base_prompt: &str) -> String {
    format!(
        "Recovery protocol: retry the previous request with the same mode and constraints.\nPreserve any valid prior work, but re-run the task end-to-end where needed.\n\nOriginal request:\n{base_prompt}"
    )
}

pub(crate) fn compose_resume_prompt(base_prompt: &str) -> String {
    format!(
        "Recovery protocol: resume from the latest incomplete boundary.\nDo not restart discovery from scratch. Preserve prior verified work, artifacts, decisions, and constraints.\n\nOriginal request:\n{base_prompt}"
    )
}

pub(crate) fn compose_restart_stage_prompt(
    base_prompt: &str,
    target: &StageRecoveryTarget,
) -> String {
    let stage_name = target
        .checkpoint
        .stage
        .as_deref()
        .unwrap_or(target.checkpoint.label.as_str());
    let profile = target
        .checkpoint
        .scheduler_profile
        .as_deref()
        .unwrap_or("scheduler");
    let stage_ref = match (target.checkpoint.stage_index, target.checkpoint.stage_total) {
        (Some(index), Some(total)) => format!("{stage_name} ({index}/{total})"),
        _ => stage_name.to_string(),
    };
    let detail = short_recovery_text(&target.body, 1200)
        .unwrap_or_else(|| "No prior stage output available.".to_string());
    format!(
        "Recovery protocol: restart scheduler stage `{stage_ref}` in preset `{profile}`.\nRe-enter this stage as a fresh boundary. Preserve global constraints and prior validated upstream context, but allow this stage and all downstream work to be recomputed from here.\n\nPrevious stage outcome:\n{detail}\n\nOriginal request:\n{base_prompt}"
    )
}

pub(crate) fn compose_stage_recovery_prompt(
    base_prompt: &str,
    target: &StageRecoveryTarget,
) -> String {
    let stage_name = target
        .checkpoint
        .stage
        .as_deref()
        .unwrap_or(target.checkpoint.label.as_str());
    let profile = target
        .checkpoint
        .scheduler_profile
        .as_deref()
        .unwrap_or("scheduler");
    let stage_ref = match (target.checkpoint.stage_index, target.checkpoint.stage_total) {
        (Some(index), Some(total)) => format!("{stage_name} ({index}/{total})"),
        _ => stage_name.to_string(),
    };
    let summary = target
        .checkpoint
        .summary
        .as_deref()
        .unwrap_or("No prior stage summary available.");
    let detail = short_recovery_text(&target.body, 1200).unwrap_or_else(|| summary.to_string());
    format!(
        "Recovery protocol: partial replay from scheduler stage `{stage_ref}` in preset `{profile}`.\nRestart from this stage boundary only. Preserve all prior valid work and replay only the downstream work required after this stage.\n\nPrevious stage outcome:\n{detail}\n\nOriginal request:\n{base_prompt}"
    )
}

pub(crate) fn compose_subtask_recovery_prompt(
    base_prompt: &str,
    target: &SubtaskRecoveryTarget,
) -> String {
    let label = if target.description.trim().is_empty() {
        target.checkpoint.label.clone()
    } else {
        target.description.clone()
    };
    format!(
        "Recovery protocol: restart subtask `{label}`.\nReplay only this subtask boundary and the minimum downstream integration work. Preserve all other completed work and constraints.\n\nOriginal request:\n{base_prompt}"
    )
}

