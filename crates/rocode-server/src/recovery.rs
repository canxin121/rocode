    #[test]
    fn protocol_only_allows_actions_advertised_by_current_state() {
        let protocol = SessionRecoveryProtocol {
            session_id: "ses_1".to_string(),
            status: RecoveryProtocolStatus::Recoverable,
            active_execution_count: 0,
            pending_question_count: 0,
            summary: None,
            last_user_prompt: Some("Fix it".to_string()),
            actions: vec![RecoveryActionInfo {
                kind: RecoveryActionKind::RestartStage,
                label: "Restart stage · Plan".to_string(),
                description: "restart".to_string(),
                target_id: Some("msg_1".to_string()),
                target_kind: Some("stage".to_string()),
            }],
            checkpoints: Vec::new(),
        };

        assert!(protocol_allows_recovery_action(
            &protocol,
            &RecoveryActionKind::RestartStage,
            Some("msg_1")
        ));
        assert!(!protocol_allows_recovery_action(
            &protocol,
            &RecoveryActionKind::RestartStage,
            Some("msg_other")
        ));
        assert!(!protocol_allows_recovery_action(
            &protocol,
            &RecoveryActionKind::Retry,
            None
        ));
    }

