fn short_recovery_text(text: &str, max_chars: usize) -> Option<String> {
    let clean = text.trim();
    if clean.is_empty() {
        return None;
    }
    let mut out = String::new();
    for (count, ch) in clean.chars().enumerate() {
        if count >= max_chars {
            out.push('…');
            break;
        }
        out.push(ch);
    }
    Some(out)
}

/// Extract the text visible to the user from an assistant message.
///
/// Delegates to `crate::routes::stream::assistant_visible_text` when called
/// from the route layer. Here we accept a pre-extracted body string to keep
/// this module independent of the session message internals.
fn assistant_visible_text_from_message(message: &rocode_session::SessionMessage) -> String {
    let mut out = String::new();
    for part in &message.parts {
        if let rocode_session::PartType::Text { text, ignored, .. } = &part.part_type {
            if ignored.unwrap_or(false) {
                continue;
            }
            out.push_str(text);
        }
    }
    out
}

pub(crate) fn latest_user_prompt(session: &rocode_session::Session) -> Option<String> {
    session.messages.iter().rev().find_map(|message| {
        if !matches!(message.role, rocode_session::MessageRole::User) {
            return None;
        }

        #[derive(Debug, Default, serde::Deserialize)]
        struct UserPromptMetadataWire {
            #[serde(
                default,
                deserialize_with = "rocode_types::deserialize_opt_string_preserve"
            )]
            resolved_user_prompt: Option<String>,
        }

        let meta: UserPromptMetadataWire = rocode_types::parse_map_lossy(&message.metadata);

        meta.resolved_user_prompt
            .as_deref()
            .map(str::trim)
            .filter(|value| !value.is_empty())
            .map(ToString::to_string)
            .or_else(|| {
                let text = message.get_text().trim().to_string();
                if text.is_empty() {
                    None
                } else {
                    Some(text)
                }
            })
    })
}

fn latest_assistant_finish(session: &rocode_session::Session) -> Option<String> {
    session
        .messages
        .iter()
        .rev()
        .find(|message| matches!(message.role, rocode_session::MessageRole::Assistant))
        .and_then(|message| message.finish.clone())
}

